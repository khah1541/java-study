package ex;

public class Ex9 {
	public static void main(String[] args) {
		//속도(m/s) = 거리(m) / 시간 (s)
		
		double meter = 100;
		double sec = 18;
		
		double ms = meter/sec;
		
		
		//속도 (km/h) = 거리(km) / 시간(h)
		
		double m = 100;
		double s = 5;
		
		
		double sisok = m/s;
		
		//결과출력
		System.out.println(sisok + "km/h");
	}

}
